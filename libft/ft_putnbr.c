#include "libft.h"

void	ft_putnbr(int nb)
{
	char	str[12];
	int		i;

	i = 11;
	if (nb == 0)
		str[--i] = '0';
	if (nb < 0)
	{
		while (nb != 0)
		{
			str[--i] = '0' - (nb % 10);
			nb = (nb / 10);
		}
		str[--i] = '-';
	}
	else
	{
		while (nb != 0)
		{
			str[--i] = '0' + (nb % 10);
			nb = (nb / 10);
		}
	}
	ft_putstr(&str[i]);
}
