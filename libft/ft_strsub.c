#include "libft.h"

char		*ft_strsub(char const *s, unsigned int start, size_t len)
{
	char	*str;

	if (len <= 0)
		return (ft_strnew(0));
	str = ft_strnew(len);
	if (s && str && ft_strlen(s) >= len)
		str = ft_strncpy(str, &s[start], len);
	return (str);
}
