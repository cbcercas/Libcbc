#include "libft.h"

size_t	ft_nbword(char const *s, char c)
{
	int		i;
	size_t	nbword;

	i = 0;
	nbword = 0;
	while (s[i] != '\0')
	{
		if ((i == 0 && s[i] != c && s[i] != '\0')
				|| (s[i] == c && s[i + 1] != '\0' && s[i + 1] != c))
			nbword++;
		i++;
	}
	return (nbword);
}
