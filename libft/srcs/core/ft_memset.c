#include "libft.h"

void	*ft_memset(void *b, int c, unsigned long n)
{
	unsigned char *tmp;

	tmp = (unsigned char *)b;
	while (n--)
	{
		*tmp = (unsigned char)c;
		tmp++;
	}
	return (b);
}
