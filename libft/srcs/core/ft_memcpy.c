#include "libft.h"

void	*ft_memcpy(void *s1, const void *s2, size_t n)
{
	unsigned char *dest;
	unsigned char *src;

	dest = (unsigned char *)s1;
	src = (unsigned char *)s2;
	while (n)
	{
		*dest = (unsigned char)*src;
		dest++;
		src++;
		n--;
	}
	return (s1);
}
