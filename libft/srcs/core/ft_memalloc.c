#include "libft.h"

void	*ft_memalloc(size_t size)
{
	char *mem;

	if (!size)
		return (NULL);
	mem = (char *)malloc(size);
	if (!mem)
		return (0);
	ft_bzero(mem, size);
	return (mem);
}
